/**
 * Сущность заявки на вакансию (Application)
 * Описывает структуру данных заявки кандидата на вакансию
 */

export class Application {
  /** Уникальный идентификатор заявки */
  id: number;

  /** ID вакансии, на которую подана заявка */
  jobId: number;

  /** Ссылка на резюме кандидата */
  resumeLink: string;

  /** Дата и время подачи заявки */
  appliedAt: Date;
}
